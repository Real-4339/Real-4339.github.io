        <h1 class="h1_0">Záver</h1>
        <br />
        <hr />
        <p>Nech tento článok vyzerá akokoľvek kriticky, iPhone XR sa mi páči.
          Konečne opäť farebne atraktívny mobil s nenormálnym výkonom, najlepšou 
          výdržou nielen medzi iPhonmi, ale snáď aj všetkými smartfónmi na trhu,
          ktorý sa nebojí vody, nahráte ním top video, nasnímate krásne relatívne
          širokouhlé zábery s rozostrením pozadia za osobami snímanými či zadnou, 
          či prednou kamerou. Pri listovaní snímkami kritizujem veľmi slabé využitie
          úzkeho displeja, aj na sledovanie Full HD videí by som si radšej vybral iPhone 
          8 Plus, ktorý je oproti XR reálne o stovku lacnejší a prináša viacero výhod.</p>
        <p>Lenže, vy túžite po tom novom iPhone s displejom rozliatym po celej
          prednej ploche, nezáleží vám na najjemnejšom displeji na svete,
          veď to by ste už mali dávno vo vrecku XS alebo XS Max. Chcete moderný dizajn, 
          pekné farby, top výkon, ktorý zabezpečí bezchybné fungovanie aj o dva - 
          tri roky a nechcete mať s mobilom žiadne bežné starosti. A v takom prípade berte XR.
          Experiment, ktorý sa vydaril. Len mi je ľúto, že nemá Full HD+ displej.</p>
