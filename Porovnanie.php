        <h1 class="h1_0">Apple iPhone XR - farebný experiment</h1>
        <br />
        <hr />
        <p>iPhone XR je zvláštnym pokusom predstaviť popri dvoch drahých modeloch XS 
            a XS Max dostupnejší jablkový smartfón, hoci aj ten v 256 GB verzii prekračuje
            tisíceurovú hranicu. Začína na cenovke 869 eur a najväčší zmysel nám dáva ako
            128 GB variácia za 929 eur.</p>
        <p>Za 949 eur však môžete mať iPhone 8 Plus v 256 GB verzii, a preto ho nižšie 
            s novým iPhonom XR viackrát porovnáme.</p>
        <p>Mimochodom, skúste XR čítať inak než desať-er,
          rovno poviem, že iks-er, a hneď zistíte, koľko ortodoxných Apple 
          fanúšikov máte okolo seba. Fascinuje ma, koľkým ľuďom záleží na takej hlúposti.</p>
        <br>
        <img style="width: 440px; border-radius: 6px;" src="Gif/Originals/1p.jpg" alt="image description" title="image tooltip"/>
        <br />
        <p>Na smartfónoch a tabletoch môžete všetky obrázky v článku priblížiť
          pomocou dvoch prstov. Na počítači môžete obrázok klikom presunúť do popredia
          a šípkami na klávesnici preskočiť na ďalší.</p>
         <p>Na začiatku vám vrelo odporúčam prečítať aj recenziu <u>iPhone XS</u> a <u>XS Max</u>.
            Drahé novinky, ktoré prinášajú hlavne vysoký výkon, vylepšené fotoaparáty, 
            XS Max tiež najsilnejšiu batériu, akú sme kedy v iPhone mohli vidieť. 
            Lenže, výdrž na jedno nabitie je ešte viac devízou dnes testovanej novinky. 
            XR po dlhom nabíjaní 1-ampérovou nabíjačkou v balení a ešte dlhšom nabíjaní 
            bezdrôtovým spôsobom (asi päť hodín od nuly do stovky) vydrží ešte viac než 
            rekordmani s prívlastkami 8 Plus či XS Max. Spoľahnúť sa môžete na dva dni bežného 
            používania. V tomto smere má, okrem veľkých iPhonov, snáď len dvoch súperov
           - Huawei Mate 20 Pro so Samsungom Galaxy Note 9 v tesnom závese.</p>   
       <br />
        <img style="width: 400px; border-radius: 6px;" src="Gif/Originals/2p.jpg" alt="image description" title="image tooltip"/>
        <br />
        <br />
        <p>Môžeme ho rovno označiť za veľmi výkonný smartfón s bohovskou výdržou na
          jedno nabitie, veľmi dobrými fotoaparátmi na fotografie i video, ktorého 
          záznam medzi smartfónmi len ťažko hľadá konkurenta. Jediným otáznikom bude 
          hrubý displej s výrazným rámom okolo. A nevyhnutné ovládanie gestami, ktorého 
          sa síce nemusíte báť, no neviem, či vám toto moje ubezpečenie bude stačiť. 
          Ale poďme pekne postupne.</p>
          <br />
          <img style="width: 400px; border-radius: 6px;" src="Gif/Originals/4p.jpg" alt="image description" title="image tooltip"/>
          <br />
         <p>Najprv si otvorte <u>základné info iPhonu XR</u>, kde nájdete všetky overené 
            parametre. Rozmery sme premerali, hmotnosť overili, chyby opravili.</p>
         <p>A teraz si môžeme vymenovať základné rozdiely nového XR oproti iPhonu 8 Plus. 
            Ten je podľa nás priamym konkurentom a súčasne aj predchodcom modelu XR.</p>
            <br />
            <img style="width: 400px; border-radius: 6px;" src="Gif/Originals/art07.jpg" alt="image description" title="image tooltip"/>
            <br />
          <p>Výkonnejší procesor A12 vyrobený 7 nm technológiou zabezpečí,
             že aj po dvoch rokoch budete mať v ruke špičkový iPhone s rýchlymi reakciami.
             Vďaka relatívne slabému displeju môžete počítať aj s vysokým reálnym výkonom 
            - procesor sa s prekresľovaním 1,5 mil. bodov potrápi oveľa menej než s dvomi
             miliónmi pixelov Full HD displeja modelu 8 Plus či dokonca 3,3 miliónmi pixelov
             na obrazovke XS Max.</p>
             <br />
             <img style="width: 400px; border-radius: 6px;" src="Gif/Originals/art08.jpg" alt="image description" title="image tooltip"/>
             <br />
         <p>Ako som už naznačil, 6,1'' IPS displej má o štvrtinu slabšie rozlíšenie než 5,5'' 
            Full HD obrazovka modelov 7 Plus a 8 Plus. V praxi nejde o žiaden strašidelný rozdiel,
           ale keď som v danom čase striedal dvestoeurové Xiaomi A2 Lite s Full HD+ displejom a tento
            štvornásobne drahší iPhone, vždy som si všimol hrubší raster XR-ka. 
            Pri dlhodobom používaní jedného mobilu vám to určite bude jedno.</p>
        <p>Mimochodom, často pracujem so snímkami obrazovky, a tak si uvedomujem,
          že odrazu mám k dispozícii namiesto 2 Mpix obrázku s pomerom 16:9 z iPhonu 
          8 Plus po orezaní na rovnaký pomer strán možno 1,3 Mpix obrázok z iPhonu XR.
         A to už je citeľný rozdiel. Ale to je len taký malý, zrejme úplne bezvýznamný postreh.</p>
         <br />
         <img style="width: 400px; border-radius: 6px;" src="Gif/Originals/art12.jpg" alt="image description" title="image tooltip"/>
         <br />
        <p>Oveľa významnejšia je absencia 3D Touch - funkcie, ktorá mi umožňuje 
         rýchlo skočiť do nastavení WiFi alebo Bluetooth po silnom stlačení ikony 
         nastavení či pohybovať kurzorom ako na touchpade silnejším stlačením prsta 
         na klávesnici. Kto 3D Touch nepoužíva, tomu chýbať nebude, ale pre mňa je to veľká zmena.
         A som presvedčený, že je nás takých viac.</p>
         <br />
         <img style="width: 400px; border-radius: 6px;" src="Gif/Originals/12p.jpg" alt="image description" title="image tooltip"/>
         <br /> 
         <p>Pri porovnaní rozmerov je iPhone XR o 2 mm užší a o 7 mm nižší než 8 Plus alebo 7 Plus.
         Reálne sa teda lepšie drží, a platí to aj pre mobil v puzdre. Ak hľadáte výkonný, 
         no kompaktný mobil, radšej sa poobzerajte po modeli XS alebo výrazne lacnejšom iPhone 8.</p>
         <br />
         <img style="width: 400px; border-radius: 6px;" src="Gif/Originals/art10.jpg" alt="image description" title="image tooltip"/>
         <br /> 
       <p>Dôležitá je aj hmotnosť mobilu. Tú si uvedomíte napríklad pri dlhšom držaní smartfónu nad hlavou
         večer alebo ráno v posteli. 194 gramov je o pár gramov viac oproti 7 Plus a o deväť gramov
         menej oproti 8 Plus. Skrátka, je to ťažká váha, a určite stojí za zváženie o takmer 20 gramov
          ľahšie XS-ko.</p>
          <p>Ak na iPhone XR prechádzate z nejakého staršieho iPhonu,
             potešíte sa nielen vyššiemu výkonu, ale aj novému zadnému fotoaparátu. 
             Ten je síce jeden, ale lepšie využiteľný. Hlavný 12 Mpix fotoaparát 
             totiž vo všetkých tohtoročných iPhonoch ponúka kratšiu ohniskovú vzdialenosť, 
             a to len 25 mm. Doteraz mali všetky iPhony 28-milimetrovú ohniskovú vzdialenosť
             (po prepočte na kinofilm). Zábery z XS, XS Max aj XR sú širšie, viac sa toho do 
             nich z rovnakej pozície fotografa zmestí. A to sa mi veľmi páči.</p>
          <br />
          <img style="width: 400px; border-radius: 6px;" src="Gif/Originals/art31.jpg" alt="image description" title="image tooltip"/>
          <br /> 
        <p>XR oproti iPhonu 7 Plus, 8 Plus, X, XS aj XS Max neponúka druhý približujúci fotoaparát.
             Lenže ten ste pri dvojnásobnom priblížení využili len zriedkakedy.
              Za bežných okolností sa totiž zakaždým využíva výrez z hlavného snímača, 
              až pri veľmi, veľmi dobrom svetle použije iPhone druhý aparát.
               Jeho využitie je tak najčastejšie pri portrétoch.</p>
        <p>Lenže, tie zvláda aj model XR. 
            A dokonca nemusíte ísť ani ďalej od objektu kvôli dvojnásobnému priblíženiu. 
            Nevýhodou je, že napríklad taký iPhone XS (Max) dokáže krásne rozostriť pozadie 
            aj za kolobežkou (pozrite si recenziu <u>SXT Neo</u>, v ktorej sú všetky zábery vytvorené iPhonom XS Max),
             čo XR nedokáže. Rozostrenie pozadia je tu možné len pri ľudských portrétoch.</p>
         <p>Fotoaparát iPhonu XR spĺňa najvyššie nároky,
         v zázname videa dosahuje ešte viac úspechov. 
         Jedinými nedostatkami ostávajú nastavenia kamery len v rámci nastavení mobilu a pomer 
         strán 4:3, ktorý neviete zmeniť. Nerozumiem, prečo je v týchto veciach Apple taký tvrdohlavý.</p>
         <br />
         <img style="width: 400px; border-radius: 6px;" src="Gif/Originals/art35.jpg" alt="image description" title="image tooltip"/>
         <br /> 
         <p>Často mením fps záznamu videa, večer si totiž veľa zábavy so 4K 60 fps neužijem.
         A tak znižujem na 30 fps a zapínam novú funkciu zníženia kadencie snímok na 24 fps pri
          veľmi zlých svetelných podmienkach. Spolu so stereo zvukom ide o najväčšie foto/video
         výhody oproti starším iPhonom.</p>
         <p>Veľkou zmenou je odomykanie tvárou, ktoré nahrádza odtlačok prsta na starších modeloch.
         Vďaka infračervenej kamere je spoľahlivé aj v noci, pričom táto druhá kamera je využívaná 
         aj pri rozostrovaní pozadia autoportrétov. Všetko je teda tak, ako má byť.</p>
         <br />
         <img style="width: 400px; border-radius: 6px;" src="Gif/Originals/art21.jpg" alt="image description" title="image tooltip"/>
         <br /> 
       <p>Zodvihnete mobil, ten sa automaticky rozsvieti, rozpozná vašu tvár,
         vy si zatiaľ prečítate notifikácie, ktoré sú zgrupené, ale napríklad e-maily 
         môžete vidieť každý samostatne a potom buď zasvietite baterku, spustíte fotoaparát 
         (stlačením jeho ikony alebo odsunutím z pravej strany) či preskočíte ťahom prsta na 
         domácu obrazovku.</p>
        <p>Oproti iPhonu 8 Plus tu máme teda navyše aj odomykanie tvárou,
         aj rozostrovanie pozadia pri portrétoch vytvorených prednou kamerou.
          Keď k tomu pripočítame menšiu ohniskovú vzdialenosť zadnej kamery, 
         záznam videa so stereo zvukom a lepšie večerné video zábery, uvedomíme si,
         že ide o veľký pokrok oproti možnostiam minuloročných iPhonov.</p>
        <p>Škoda len, že pri prezeraní fotografií využívate asi len 70 % úzkeho displeja, 
        vidíte ich totiž len na ploche 1104 x 828 pixelov (0,91 Mpix), pričom na iPhone 7 Plus 
        alebo 8 Plus sa fotografia zobrazuje na ploche 1440 x 1080 pixelov (1,56 Mpix).</p>
        <br />
         <img style="width: 400px; border-radius: 6px;" src="Gif/Originals/art27.jpg" alt="image description" title="image tooltip"/>
         <br />
         <p>A to je o 70 % viac na o dva roky staršom a o dvesto eur lacnejšom mobile 
            (iPhone 7 Plus v 128 GB verzii kúpite za 749 eur). Väčšiu plochu a viac pixelov 
            oceníte nielen pri prezeraní, ale aj úprave fotografií a videí.</p>
            <p>Chýbať vám možno bude "tlačidlo" s čítačkou odtlačkov pod displejom,
             hoci ja som si naň za celý týždeň spomenul možno raz. Ťažko povedať,
             ako ste na tom vy. Ovládanie gestami je spoľahlivé a nemusíte sa ho obávať.</p>
             <p>Užší displej prináša obmedzenie nielen pri prezeraní fotografií, 
            ale aj čítaní textu. Ten sa oproti iPhonu 7 Plus / 8 Plus zalamuje o pár znakov
          skôr, rovnaký odsek recenzie tak potrebuje viac riadkov a tým pádom vidíte na dlhšej
           obrazovke väčšinou to isté, ako na svojom staršom pluskovom iPhone.
         Skrátka, niekedy toho na displeji vidíte o trochu viac (facebook, web),
          niekedy však ani to, a po otvorení vysoko položenej virtuálnej klávesnice
           natiahnutý displej stráca význam úplne. To ma trocha mrzí.</p>
           <p>A ani tu nikde nevidím percento batérie, vždy pre kontrolu jej stavu
         musím stiahnuť ovládacie centrum z pravého horného rohu alebo notifikačné 
         centrum z ľavého. A to je fakt zbytočne nepraktické.</p>
         <p><u>Veľkou výhodou XR-ka je dostupnosť nielen vo viacerých farebných verziách, 
            ale aj s rôznymi úložiskami - 64 GB, 128 GB a 256 GB. A práve zlatá stredná 
        cesta bude zrejme často tá najvhodnejšia. XS alebo XS Max sa predávajú len 
        ako 64-ky, 256-ky a 512-ky, čo je práve rybník pre žralokov so 128-gigabajtovým úložiskom.</u></p>
        <p><b>Výhody:</b>

            <p>+ nič výkonnejšie na trhu nenájdete</p>
            
            <p> + výborný zadný aj predný fotoaparát</p>
            
            <p> + záznam 4K 60 fps videa so stereo zvukom</p>
            
            <p> + pestré farebné verzie</p>
            
            <p> + rozumné pamäťové verzie</p>
            
            <p> + výborná výdrž na jedno nabitie</p></p>

            <p><b>Nevýhody:</b>

                <p> - na túto cenovú kategóriu slabé rozlíšenie displeja</p>
                
                <p> - pomalé nabíjanie dodávanou nabíjačkou</p>
                
                <p> - fotografie len s pomerom 4:3</p>
                
                <p> - snímky pri prezeraní využívajú len asi 70 % plochy natiahnutého displeja</p>
                
                <p> - všeobecne slabé využitie natiahnutého displeja</p>
                
                <p> - percento batérie uvidíte až po stiahnutí hornej lišty</p>
                
                <p> - umelé rozostrenie pozadia len za osobami</p>
                
                <p> - chýba 3D Touch</p>
                <strong>Orientačná cena</strong>: 64 GB za 869 eur, 128 GB za 929 eur, 256 GB za 1039 eur</p>
